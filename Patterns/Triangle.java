
public class Triangle{
    public static void main(String[] args) {
        System.out.println("\n\t Triangle pattern \n");
        int n=6;
        for(int i=0;i<n;i++){
            for(int j=0;j<=i;j++){
                System.out.print("\t*");
            }
            System.out.println("\n");
        }
    }
}

