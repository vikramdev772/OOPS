public class Reversetri {
    public static void main(String[] args) {
        System.out.println("\n\t Reverse Triangle \n");
        int n=6;
        for(int i=1;i<n;i++){
            for(int j=n;j>i;j--){
                System.out.print("\t*");
            }
            System.out.println("\n");
        }
    }   
}

