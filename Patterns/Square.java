public class Square {
    public static void main(String[] args) {
        System.out.println("\n\t Square pattern \n");
        int n=5;
        for(int i=0;i<n;i++){
            for(int j=0;j<n;j++){
                System.out.print("\t*");
            }
            System.out.println("\n");
        }
    }
}
