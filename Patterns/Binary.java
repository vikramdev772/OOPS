public class Binary {
    public static void main(String[] args) {
        System.out.println("\n\t Binary Square \n");
        int i,j;
        int n=5;
        for(i=1;i<=n;i++){
            for(j=1;j<=n;j++){
                System.out.print("\t "+j%2+" ");
            }
            System.out.println("\n");
        }

    }
    
}
