public class While {
    public static void main(String[] args) {
        System.out.println("\n\t While loop ..! \n");

        int num = 1;
        while(num <= 5){
            System.out.println("\n\t"+num);
            num+=1;
        }

        /*Syntax 
         * 
         * while(condition){
         * 
         * //body
         * }
         */
    }
}
