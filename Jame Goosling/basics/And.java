
import java.util.*;

public class And {
    public static void main(String[] args) {
        
        //bitwise opeerators
        int a=0;
        int b=10;
        if(a==10 && b==20){  //And operator
            System.out.println("\n\t Hello world!");
        }
        else{
            
            System.out.println("\n\t Error BRO  " );
        }


        //Or operator
        if (a==10 || b==20){
            System.out.println("\n\t Stament is equal ");
        }
        else{
            System.out.println("\n\t not Equal !");
        }

    }
}